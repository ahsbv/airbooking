function toggleReturnDate(show) {
        var returnDateSection = document.getElementById('returnDateSection');
        if (show) {
            returnDateSection.style.display = 'block';
        } else {
            returnDateSection.style.display = 'none';
        }
    }
    function showForm(formId) {
        // Hide all forms
        var forms = document.getElementsByClassName('form-section');
        for (var i = 0; i < forms.length; i++) {
            forms[i].style.display = 'none';
        }

        // Show the selected form
        document.getElementById(formId).style.display = 'block';
    }

    // Toggle return date based on trip type selection
    function toggleReturnDate(show) {
        var returnDateSection = document.getElementById('returnDateSection');
        if (show) {
            returnDateSection.style.display = 'block';
        } else {
            returnDateSection.style.display = 'none';
        }
    }
